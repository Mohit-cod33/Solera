package calculator;

public class Operations {
	public void add(int n, int m) {
		System.out.println(n+m);
	}
	public void sub(int n, int m) {
		System.out.println(n-m);
	}
	public void mul(int n, int m) {
		System.out.println(n*m);
	}
	public void div(int n, int m) {
		System.out.println(n/m);
	}

}
